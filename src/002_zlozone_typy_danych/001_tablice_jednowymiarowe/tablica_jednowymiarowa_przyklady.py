# Przykłady tablic jednowymiarowych w Pythonie

# Tablica liczb całkowitych
tablica_calkowitych = [1, 2, 3, 4, 5]

# Tablica łańcuchów znaków (tekstów)
tablica_tekstow = ["jabłko", "banan", "gruszka"]

# Tablica liczb zmiennoprzecinkowych
tablica_zmiennoprzecinkowych = [3.14, 2.718, 1.618]

# Tworzenie tablicy niejednorodnej
tablica_niejednorodna = [
    "Tekst",
    123,
    3.14,
    True,
    [1, 2, 3],
    {"klucz": "wartość"},
]

# Wyświetlenie tablic
print("Tablica liczb całkowitych:", tablica_calkowitych)
print("Tablica tekstów:", tablica_tekstow)
print("Tablica liczb zmiennoprzecinkowych:", tablica_zmiennoprzecinkowych)
print("Tablica niejednorodna :", tablica_niejednorodna)