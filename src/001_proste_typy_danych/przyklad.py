# Przykłady różnych typów danych w Pythonie

# Typ numeryczny (liczba całkowita)
liczba_calkowita = 42

# Typ numeryczny (liczba zmiennoprzecinkowa)
liczba_zmiennoprzecinkowa = 3.14

# Typ logiczny (wartość logiczna True/False)
flaga = True

# Typ znakowy (znak)
znak = 'A'

# Typ łańcuchowy (ciąg znaków)
napis = "Hello, World!"

# Konkatenacja łańcuchów
komunikat = "Witaj, " + "świecie!"

# Wyświetlenie danych
print("Liczba całkowita:", liczba_calkowita)
print("Liczba zmiennoprzecinkowa:", liczba_zmiennoprzecinkowa)
print("Flaga logiczna:", flaga)
print("Znak:", znak)
print("Napis:", napis)
print("Komunikat po konkatenacji:", komunikat)
